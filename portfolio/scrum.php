<p>
    Als project voor school hebben wij in opdracht van InfoSupport een digitaal SCRUM bord gemaakt.
	Dit is bedoeld om de workflow bij projecten overzichtelijk te maken, in een opslag kan worden gezien wat nog gedaan moet worden en wat al gedaan is.
	Alle data wordt op een door school aangeleverde server opgeslagen zodat de applicatie vanaf meerdere locaties gebruikt kan worden.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
	<li>Wijzigen van items</li>
	<li>Toevoegen van items</li>
    <li>Slepen van items</li>
	<li>Itemgeschiedenis bekijken</li>
	<li>Grafieken over projectverloop bekijken</li>
</ul>
