<p>
	Ik heb samen met vijf anderen als project voor een school een Sudoku applicatie gemaakt. 
	De bedoeling van deze applicatie was dat deze goed zou werken op mobiele apparaten. 
	Wij hebben hiervoor als oplossing een Sudoku gemaakt die in de browser gebruikt kan worden. 
	Hierdoor kan je op elk apparaat met een browser het spel spelen. 
	Omdat het alleen client-side talen gebruikt kan het zowel lokaal als op een server gedraaid worden.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
    <li>Opslaan en laden</li>
    <li>Verschillende moeilijkheidsgraden</li>
    <li>Notities maken</li>
    <li>Hints en automatisch oplossen</li>
    <li>Een leeg bord invullen en op laten lossen</li>
</ul>
