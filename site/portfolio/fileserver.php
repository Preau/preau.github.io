<p>
    Als opdracht voor school hebben wij een fileserver en client gemaakt.
	De server en client communiceren met elkaar en door middel van commando's kan de client acties uitvoeren op de bestanden die op de server staan.
	De server is geschreven in C++ en de client in C#.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
	<li>Ophalen en versturen van bestanden</li>
	<li>Hernoemen van bestanden</li>
    <li>Verwijderen van bestanden</li>
	<li>Synchroniseren van mappen tussen client en server</li>
	<li>Server werkt op Windows en Linux</li>
</ul>
<span class="subheadertext">Gebruikte talen</span>
<ul>
    <li>C++</li>
    <li>C#</li>
</ul>