<p>
    De Academie voor Management & Bestuur van Avans Hogeschool heeft ons gevraagd om als project het proces van het inplannen van afstudeerzittingen te automatiseren.
    De applicatie die wij gemaakt hebben bevat onder andere een automatische planner, importeren van personenlijsten en automatische e-mail functies. 
</p>
<p>
    De applicatie is geschreven in C#, daarnaast is er een webgedeelte dat in PHP geschreven is.
    De beide delen kunnen met elkaar communiceren door middel van XML.
    Alle data wordt opgeslagen in een MySQL database.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
    <li>Automatisch inplannen zittingen</li>
	<li>Importeren van studenten, docenten, bedrijfsbegeleiders en externe deskundigen</li>
	<li>Automatisch e-mails versturen</li>
</ul>
<span class="subheadertext">Gebruikte talen</span>
<ul>
    <li>C#</li>
    <li>PHP</li>
    <li>HTML</li>
    <li>CSS</li>
    <li>MySQL</li>
    <li>XML</li>
</ul>