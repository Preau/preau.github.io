<p>
    Voor Habotech heb ik een webwinkel gemaakt. 
    Deze heb ik gemaakt met behulp van het open source e-commerce framework <a href="http://www.oscommerce.com/">osCommerce</a>. 
    Hieraan heb ik wat aanpassingen gedaan, waaronder een iDeal implementatie.
</p>
<p>
    De webwinkel is geheel te beheren door iemand zonder technische kennis.
    In het beheergedeelte kunnen onder andere producten, categorie&euml;n en klanten worden beheerd.
    De beheerder kan statistieken en rapporten van de webwinkel genereren.
</p>
