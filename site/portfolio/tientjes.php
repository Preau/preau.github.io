<p>
    Website gemaakt voor de oprichters van Tientjes.net als schoolproject, de eerste keer dat we op school voor een echte klant werkten. 
    De site bevat onder andere een zelfgemaakt CMS en rechtensysteem, de voorkant wordt gemaakt met behulp van de <a href="http://www.smarty.net/">Smarty</a> template engine.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
    <li>Plaatsen van advertenties</li>
	<li>Agenda</li>
	<li>Beheerpanel met rechten, paginabeheer, nieuwsbrieven en siteinstellingen</li>
	<li>Twitterfeed</li>
</ul>
