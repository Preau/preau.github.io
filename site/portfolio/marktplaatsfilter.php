<p>
    Ik heb deze filter gemaakt omdat ik mezelf ergerde aan het grote aantal handelaren op Marktplaats. 
    Het begon als een simpel stukje JavaScript maar uit interesse heb ik het omgebouwd naar een userscript zodat het in alle browsers werkt en makkelijk in gebruik is.
</p>
<p>
    Hiermee is het mogelijk om zoekresultaten op Marktplaats te filteren op bepaalde termen. 
    Deze termen kunnen door de gebruiker worden gekozen en de zoekresultaten worden dan automatisch verwijderd op Marktplaats. 
	Ook is het mogelijk om bij elke advertentie de verkoper eenvoudig toe te voegen aan de filters.
</p>
<span class="subheadertext">Functionaliteit</span>
<ul>
    <li>Filter zoekresultaten op woorden</li>
	<li>Gemakkelijk verkopers toevoegen aan filter</li>
    <li>Hoofdlettergevoeligheid optie</li>
    <li>Opslag instellingen in local storage</li>
</ul>
